# terminologies:
    # Hypothesis setting up:
        # H0: default, assumed
        # Ha: trying to prove, complementary to H0
    # one-side vs. two-side hypothesis
    # errors:
        # type1: reject H0 when H0 is true
            # alpha, significance level
        # type2: fail to reject H0 when H0 is false
    # p value: the probability of happening if H0 is true
    # confidence level: 1 - p-value


